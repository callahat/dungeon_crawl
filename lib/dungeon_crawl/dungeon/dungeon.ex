defmodule DungeonCrawl.Dungeon do
  alias Ecto.Multi

  @moduledoc """
  The Dungeon context.
  """

  import Ecto.Query, warn: false
  alias DungeonCrawl.Repo

  alias DungeonCrawl.Dungeon.Map
  alias DungeonCrawl.Dungeon.MapTile

  alias DungeonCrawl.TileTemplates.TileSeeder

  @doc """
  Returns the list of dungeons.

  ## Examples

      iex> list_dungeons()
      [%Map{}, ...]

  """
  def list_dungeons do
    Repo.all(Map)
  end

  @doc """
  Gets a single map.

  Raises `Ecto.NoResultsError` if the Map does not exist.

  ## Examples

      iex> get_map!(123)
      %Map{}

      iex> get_map!(456)
      ** (Ecto.NoResultsError)

  """
  def get_map(id),  do: Repo.get(Map, id)
  def get_map!(id), do: Repo.get!(Map, id)

  def get_map_by(attrs), do: Repo.get_by!(Map, attrs)

  @doc """
  Creates a map.

  ## Examples

      iex> create_map(%{field: value})
      {:ok, %Map{}}

      iex> create_map(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_map(attrs \\ %{}) do
    %Map{}
    |> Map.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Autogenerates a map.

  ## Examples

      iex> generate_map(DungeonGenerator, %{field: value})
      {:ok, %Map{}}

      iex> generate_map(DungeonGenerator, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def generate_map(dungeon_generator, attrs \\ %{}) do
    Multi.new
    |> Multi.insert(:dungeon, Map.changeset(%Map{}, attrs) |> Ecto.Changeset.put_change(:autogenerated, true))
    |> Multi.run(:dungeon_map_tiles, fn(%{dungeon: dungeon}) ->
        result = Repo.insert_all(MapTile, _generate_dungeon_map_tiles(dungeon, dungeon_generator))
        {:ok, result}
      end)
    |> Repo.transaction()
  end

  defp _generate_dungeon_map_tiles(dungeon, dungeon_generator) do
    tile_mapping = TileSeeder.basic_tiles()

    dungeon_generator.generate(dungeon.height, dungeon.width)
    |> Enum.to_list
    |> Enum.map(fn({{row,col}, tile}) -> %{dungeon_id: dungeon.id, row: row, col: col, tile: tile_mapping[tile].character, tile_template_id: tile_mapping[tile].id} end)
  end

  @doc """
  Updates a map.

  ## Examples

      iex> update_map(map, %{field: new_value})
      {:ok, %Map{}}

      iex> update_map(map, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_map(%Map{} = map, attrs) do
    map
    |> Map.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a Map.

  ## Examples

      iex> delete_map(map)
      {:ok, %Map{}}

      iex> delete_map(map)
      {:error, %Ecto.Changeset{}}

  """
  def delete_map(%Map{} = map) do
    Repo.delete(map)
  end
  def delete_map!(%Map{} = map) do
    Repo.delete!(map)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking map changes.

  ## Examples

      iex> change_map(map)
      %Ecto.Changeset{source: %Map{}}

  """
  def change_map(%Map{} = map) do
    Map.changeset(map, %{})
  end

  @doc """
  Returns the list of dungeon_map_tiles.

  ## Examples

      iex> list_dungeon_map_tiles()
      [%MapTile{}, ...]

  """
  def list_dungeon_map_tiles do
    Repo.all(MapTile)
  end

  @doc """
  Gets a single map_tile.

  Raises `Ecto.NoResultsError` if the Map tile does not exist.

  ## Examples

      iex> get_map_tile!(123)
      %MapTile{}

      iex> get_map_tile!(456)
      ** (Ecto.NoResultsError)

  """
  def get_map_tile!(%{dungeon_id: dungeon_id, row: row, col: col}, direction) do
    {d_row, d_col} = _direction_delta(direction)
    get_map_tile!(dungeon_id, row + d_row, col + d_col)
  end
  def get_map_tile!(%{dungeon_id: dungeon_id, row: row, col: col}), do: get_map_tile!(dungeon_id, row, col)
  def get_map_tile!(dungeon_id, row, col, direction), do: get_map_tile!(%{dungeon_id: dungeon_id, row: row, col: col}, direction)
  def get_map_tile!(dungeon_id, row, col), do: Repo.get_by!(MapTile, %{dungeon_id: dungeon_id, row: row, col: col})
  def get_map_tile!(id), do: Repo.get!(MapTile, id)

  def get_map_tile(%{dungeon_id: dungeon_id, row: row, col: col}, direction) do
    {d_row, d_col} = _direction_delta(direction)
    get_map_tile(dungeon_id, row + d_row, col + d_col)
  end
  def get_map_tile(%{dungeon_id: dungeon_id, row: row, col: col}), do: get_map_tile(dungeon_id, row, col)
  def get_map_tile(dungeon_id, row, col, direction), do: get_map_tile(%{dungeon_id: dungeon_id, row: row, col: col}, direction)
  def get_map_tile(dungeon_id, row, col), do: Repo.get_by(MapTile, %{dungeon_id: dungeon_id, row: row, col: col})

  defp _direction_delta(direction) do
    case direction do
      "up"    -> {-1,  0}
      "down"  -> { 1,  0}
      "left"  -> { 0, -1}
      "right" -> { 0,  1}
      _       -> { 0,  0}
    end
  end

  @doc """
  Creates a map_tile.

  ## Examples

      iex> create_map_tile(%{field: value})
      {:ok, %MapTile{}}

      iex> create_map_tile(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_map_tile(attrs \\ %{}) do
    %MapTile{}
    |> MapTile.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a map_tile.

  ## Examples

      iex> update_map_tile(map_tile, %{field: new_value})
      {:ok, %MapTile{}}

      iex> update_map_tile(map_tile, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_map_tile(%MapTile{} = map_tile, new_tile) do
    map_tile
    |> MapTile.changeset(%{tile: new_tile})
    |> Repo.update()
  end
  def update_map_tile(%{dungeon_id: dungeon_id, row: row, col: col}, new_tile) do
    update_map_tile(get_map_tile!(dungeon_id, row, col), new_tile)
  end

  # TODO: remove the guard clause when the other tile update method is removed
  def update_map_tile!(%MapTile{} = map_tile, new_tile) when is_integer(new_tile) do
    map_tile
    |> MapTile.changeset(%{tile_template_id: new_tile})
    |> Repo.update!
  end
  def update_map_tile!(%{dungeon_id: dungeon_id, row: row, col: col}, new_tile) when is_integer(new_tile) do
    update_map_tile!(get_map_tile!(dungeon_id, row, col), new_tile)
  end

  # TODO: remove this
  def update_map_tile!(%MapTile{} = map_tile, new_tile) do
    map_tile
    |> MapTile.changeset(%{tile: new_tile})
    |> Repo.update!
  end
  def update_map_tile!(%{dungeon_id: dungeon_id, row: row, col: col}, new_tile) do
    update_map_tile!(get_map_tile!(dungeon_id, row, col), new_tile)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking map_tile changes.

  ## Examples

      iex> change_map_tile(map_tile)
      %Ecto.Changeset{source: %MapTile{}}

  """
  def change_map_tile(%MapTile{} = map_tile) do
    MapTile.changeset(map_tile, %{})
  end
end
