defmodule DungeonCrawl.DungeonTest do
  use DungeonCrawl.ModelCase

  alias DungeonCrawl.Dungeon

  @valid_attrs %{name: "some content"}
  @invalid_attrs %{}

  test "changeset with valid attributes" do
    changeset = Dungeon.changeset(%Dungeon{}, @valid_attrs)
    assert changeset.valid?
  end

  test "changeset with invalid attributes" do
    changeset = Dungeon.changeset(%Dungeon{}, @invalid_attrs)
    refute changeset.valid?
  end

  test "on_delete deletes all associated player_locations" do
    dungeon = insert_autogenerated_dungeon()
    player_location = insert_player_location(%{dungeon_id: dungeon.id})
    assert {:ok, _dungeon} = Repo.delete(dungeon)
    refute Repo.get_by(DungeonCrawl.PlayerLocation, %{dungeon_id: dungeon.id})
  end
end
